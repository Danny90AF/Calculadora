
package calc;

public class Multiplicacion {
    float valor1,valor2,total;

    public Multiplicacion(float va1, float va2) {
        this.valor1 = va1;
        this.valor2 = va2;
    }
    
    
    public Multiplicacion(){}
    
    
    
    public float Multiplicar(){
    
    total = (valor1 * valor2);
    
    return total; 
    }
}
